    CD_FNDO_GRTR        INTEGER,
    CD_AGT_FNCO         INTEGER,
    CD_TIP_PGM_CRD      INTEGER,
    CD_IDFR_EXNO_OPR    VARCHAR(30),
    CD_TIP_EST_OPR      INTEGER,
    NM_TIP_PNC_OPR_CRD  VARCHAR(60),
    DT_SNC_PHC          DATE
);

-- ================================================================
-- DB2GFG — Dominio / Transacional
-- ================================================================
CREATE TABLE IF NOT EXISTS DB2GFG.AGT_FNCO (
    CD_AGT_FNCO         INTEGER NOT NULL PRIMARY KEY,
    CD_CLI              INTEGER NOT NULL,
    NM_ABVD_AGT_FNCO    VARCHAR(60) NOT NULL
);

CREATE TABLE IF NOT EXISTS DB2GFG.FNDO_GRTR (
    CD_FNDO_GRTR        INTEGER NOT NULL PRIMARY KEY,
    NM_FNDO_GRTR        VARCHAR(80) NOT NULL,
    SG_FNDO_GRTR        VARCHAR(10)
);

CREATE TABLE IF NOT EXISTS DB2GFG.AGT_FNCO_FNDO_GRTR (
    CD_AGT_FNCO         INTEGER NOT NULL,
    CD_FNDO_GRTR        INTEGER NOT NULL,
    CD_TIP_EST_AGT      SMALLINT NOT NULL DEFAULT 1,
    CD_TIP_ITCB_FNCO    SMALLINT NOT NULL DEFAULT 1,
    PRIMARY KEY (CD_AGT_FNCO, CD_FNDO_GRTR)
);

CREATE TABLE IF NOT EXISTS DB2GFG.TIP_EST_RMS (
    CD_TIP_EST_RMS      SMALLINT NOT NULL PRIMARY KEY,
    NM_TIP_EST_RMS      VARCHAR(100) NOT NULL
);

CREATE TABLE IF NOT EXISTS DB2GFG.TIP_MTV_RJC_RMS (
    CD_TIP_MTV_RJC_RMS  SMALLINT NOT NULL PRIMARY KEY,
    NM_TIP_MTV_RJC_RMS  VARCHAR(100) NOT NULL,
    TX_TIP_MTV_RJC_RMS  VARCHAR(200)
);

CREATE TABLE IF NOT EXISTS DB2GFG.TIP_MVTC_FNCR (
    CD_TIP_MVTC_FNCR    SMALLINT NOT NULL PRIMARY KEY,
    NM_TIP_MVTC_FNCR    VARCHAR(100) NOT NULL
);

CREATE TABLE IF NOT EXISTS DB2GFG.RMS_AGT_FNCO (
    CD_RMS_AGT_FNCO     INTEGER NOT NULL PRIMARY KEY,
    CD_AGT_FNCO         INTEGER NOT NULL,
    CD_FNDO_GRTR        INTEGER NOT NULL,
    NR_SEQL_RMS         SMALLINT,
    NM_DTST             VARCHAR(44),
    CD_CNP2_AGT_FNCO    VARCHAR(14),
    TS_RCBT_RMS         TIMESTAMP,
    DT_PRCT_EVT         DATE,
    DT_ATL_MNTR         DATE,
    DT_MVTC_FNCR        DATE,
    CD_MTV_RJC_RMS      SMALLINT NOT NULL DEFAULT 0,
    VL_LQDO_MVTC_RMS    DECIMAL(17,2),
    CD_TIP_NTZ_MVTC     SMALLINT,
    CD_TIP_EST_RMS      SMALLINT NOT NULL DEFAULT 1,
    QT_REG_RMS          INTEGER
);

CREATE TABLE IF NOT EXISTS DB2GFG.RSM_MVTC_FNCR_RMS (
    CD_RMS_AGT_FNCO     INTEGER NOT NULL,
    CD_TIP_MVTC_FNCR    SMALLINT NOT NULL,
    QT_MVTC_FNCR        INTEGER,
    VL_NMML_MVTC_FNCR   DECIMAL(17,2),
    VL_ATL_MNTR_MVTC    DECIMAL(17,2),
    VL_TRBT_NMML        DECIMAL(17,2),
    VL_TRBT_ATL_MNTR    DECIMAL(17,2),
    VL_LQDO_MVTD        DECIMAL(17,2),
    PRIMARY KEY (CD_RMS_AGT_FNCO, CD_TIP_MVTC_FNCR)
);

CREATE TABLE IF NOT EXISTS DB2GFG.EVT_OPR_R3TD (
    CD_RMS_AGT_FNCO     INTEGER NOT NULL,
    NR_SEQL_EVT         INTEGER NOT NULL,
    PRIMARY KEY (CD_RMS_AGT_FNCO, NR_SEQL_EVT)
);

